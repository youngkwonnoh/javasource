package ch2;

// 비교연산자 : <, <=, >, >=, ==, !=

public class CompareOperEx1 {
	public static void main(String[] args) {
		int num1 = 10, num2 = 10;
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		System.out.println(num1 <= num2);
		
		char ch1 = 'A';
		char ch2 = 'B';
		System.out.println(ch1 < ch2);
	}

}
